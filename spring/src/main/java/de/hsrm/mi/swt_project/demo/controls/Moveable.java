package de.hsrm.mi.swt_project.demo.controls;

/**
 * Classes that implement this interface represent objects whose position can change. 
 * @author Marieke Schmitz
 */

public interface Moveable {

    /**
     * Moves the object according to the given x- and y-position
     * @author Marieke Schmitz
     */
    public void move(float posX, float posY);

}
